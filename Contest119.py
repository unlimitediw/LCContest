class TreeNode():
    def __init__(self, val):
        self.val = val
        self.left = None
        self.right = None


class BST():
    def __init__(self, root):
        self.root = root

    def insert(self, val,cur = None):
        if not cur:
            cur = self.root
        if val > cur.val:
            if cur.right:
                self.insert( val,cur.right)
            else:
                cur.right = TreeNode(val)
        else:
            if cur.left:
                self.insert( val,cur.left)
            else:
                cur.left = TreeNode(val)


    def searchL(self,target,find = False,cur = None):
        print(target)
        if not cur:
            cur = self.root
        if not find:
            if cur.val > target:
                if cur.left:
                    return self.searchL(cur.val,True,cur.left)
                else:
                    return cur.val[1]
            else:
                if cur.right:
                    return self.searchL(target,False,cur.right)
                else:
                    return 0
        else:
            if cur.left:
                return self.searchL(cur.val,True,cur.left)
            else:
                return cur.val[1]

