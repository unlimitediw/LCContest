    def kClosest(self, points, K):
        """
        :type points: List[List[int]]
        :type K: int
        :rtype: List[List[int]]
        """

        heap = []

        def calDis(point):
            return -math.sqrt(point[0] ** 2 + point[1] ** 2)

        for i in range(len(points)):
            if len(heap) == K:
                cur = calDis(points[i])
                if cur > heap[0][0]:
                    heapq.heappush(heap, (cur, i))
                    heapq.heappop(heap)
            else:
                heapq.heappush(heap, (calDis(points[i]), i))
        res = []
        for val in heap:
            res.append(points[val[1]])

        return res

    def largestPerimeter(self, A):
        """
        :type A: List[int]
        :rtype: int
        """
        A.sort()
        for i in range(len(A) - 1, 1, -1):
            if A[i] < (A[i - 1] + A[i - 2]):
                return sum(A[i - 2:i + 1])
        return 0

    def subarraysDivByKnonConti(self, A, K):
        """
        :type A: List[int]
        :type K: int
        :rtype: int
        """

        for i in range(len(A)):
            A[i] %= K

        dic = {}
        for i in range(len(A)):
            if A[i] not in dic:
                dic[A[i]] = 1
            else:
                dic[A[i]] += 1
        keyList = list(dic.keys())
        memo = {}

        def search(idx, remain):
            if idx == len(keyList):
                if remain == 0:
                    return 1
                else:
                    return 0
            if (idx, remain) not in memo:
                memo[(idx, remain)] = 0
                for i in range(dic[keyList[idx]] + 1):
                    memo[(idx, remain)] += search(idx + 1, (remain - i * keyList[idx]) % K)
            return memo[(idx, remain)]

        return search(0, 0)

    def subarraysDivByKSlow(self, A, K):
        """
        :type A: List[int]
        :type K: int
        :rtype: int
        """
        pre = {}
        res = 0
        for i in range(len(A)):
            newDic = {A[i] % K: 1}
            for key, val in pre.items():
                if (key + A[i]) % K not in newDic:
                    newDic[(key + A[i]) % K] = val
                else:
                    newDic[(key + A[i]) % K] += val
            if 0 in newDic:
                res += newDic[0]
            pre = newDic
        return res

    def subarraysDivByK(self, A, K):
        """
        :type A: List[int]
        :type K: int
        :rtype: int
        """
        res = 0
        memo = [0 for _ in range(K)]
        acc = 0
        # 只有0位需要记住，acc记录求和
        for i in range(len(A)):
            acc = (acc + A[i] % K) % K
            memo[(A[i] % K) - acc] += 1  # 上一次的位置+1
            res += memo[-acc]  # 现在位置位memo[acc]的对应，相加为0
        return res

